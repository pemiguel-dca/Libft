#include "libft.h"

size_t	ft_strlcat(char *dest, char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	dlen;
	size_t	slen;

	i = 0;
	j = 0;
	if (!src && !dest)
		return (0);
	if (size == 0)
		return (ft_strlen((char *)src));
	while (dest[j] != '\0')
	{
		j++;
	}
	dlen = j;
	slen = ft_strlen(src);
	if (size == 0 || size <= dlen)
		return (slen + size);
	while (src[i] != '\0' && i < size - dlen - 1)
		dest[j++] = src[i++];
	if (src[0] != '\0')
		dest[j] = '\0';
	return (dlen + slen);
}
